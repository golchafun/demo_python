# menggunakan for dengan range
for x in range(0,5):
   print(x)
print('--------------')

# deklarasi list
list_a = [1, 3, 7, 12, 15]

# menggunakan for dengan list
for x in list_a:
   print(x)
print('--------------')

# menggunakan for dan conditional
for x in list_a:
   if(x%3 != 0):
      print(x)